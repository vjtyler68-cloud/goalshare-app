/// Days since a fixed anchor, so the index advances by exactly one each
/// calendar day and cycles through the whole list across the year.
SparkQuote sparkQuoteForDay(DateTime day) {
  // UTC midnights so DST can't collapse two calendar days onto one index: a
  // local-time difference across a DST boundary is 23h/25h and .inDays would
  // truncate, making the quote fail to advance (or skip) twice a year.
  final d = DateTime.utc(day.year, day.month, day.day);
  final epochDay = d.difference(DateTime.utc(2000, 1, 1)).inDays;
  final n = kDailySparkQuotes.length;
  final idx = ((epochDay % n) + n) % n; // safe for any date
  return kDailySparkQuotes[idx];
}

/// Today's spark.
SparkQuote get quoteOfTheDay => sparkQuoteForDay(DateTime.now());
